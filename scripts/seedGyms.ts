async function seed() {
  const collectionRef = collection(db, 'gyms');
  
  // Clear existing
  const snapshot = await getDocs(collectionRef);
  for (const gymDoc of snapshot.docs) {
    await deleteDoc(doc(db, 'gyms', gymDoc.id));
  }
  
  console.log('Seeding gyms...');
  for (const gym of gyms) {
    await addDoc(collectionRef, gym);
  }
  console.log('Seeding completed!');
}

